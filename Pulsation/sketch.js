let points = [];
let inputText = "hello";

let state = "idle"; // idle | scatter

let spacing = 10;
let letterSpacing = 25;
let resolution = 8; // metaball çözünürlük (düşük = hızlı)

function setup() {
  createCanvas(windowWidth, windowHeight);
  pixelDensity(1);
  generateText(inputText);
}

function draw() {
  background(255);

  updatePoints();
  drawMetaballs();
}

// =========================
// INPUT
// =========================
function keyPressed() {
  if (keyCode === ENTER) {
    state = "scatter";
    for (let p of points) p.scatter();
  }

  if (keyCode === BACKSPACE) {
    inputText = inputText.slice(0, -1);
    generateText(inputText);
  }
}

function keyTyped() {
  if (key.length === 1) {
    inputText += key.toLowerCase();
    generateText(inputText);
  }
}

// =========================
// POINT CLASS
// =========================
class Point {
  constructor(x, y) {
    this.origin = createVector(x, y);
    this.pos = this.origin.copy();

    this.offset = random(1000);
    this.vel = p5.Vector.random2D().mult(random(1, 3));
  }

  update() {
    if (state === "idle") {
      let pulse = sin(frameCount * 0.05 + this.offset) * 2;
      this.pos.x = this.origin.x + pulse;
      this.pos.y = this.origin.y + pulse;
    } else {
      this.pos.add(this.vel);
    }
  }

  scatter() {
    this.vel = p5.Vector.random2D().mult(random(2, 6));
  }
}

function updatePoints() {
  for (let p of points) {
    p.update();
  }
}

// =========================
// METABALL RENDER
// =========================
function drawMetaballs() {
  loadPixels();

  for (let x = 0; x < width; x += resolution) {
    for (let y = 0; y < height; y += resolution) {
      let sum = 0;

      for (let p of points) {
        let dx = x - (p.pos.x + width / 2);
        let dy = y - (p.pos.y + height / 2);
        let d = dx * dx + dy * dy;

        sum += 1000 / d;
      }

      let col = sum > 1 ? 0 : 255;

      for (let i = 0; i < resolution; i++) {
        for (let j = 0; j < resolution; j++) {
          let index = 4 * ((x + i) + (y + j) * width);
          pixels[index] = col;
          pixels[index + 1] = col;
          pixels[index + 2] = col;
          pixels[index + 3] = 255;
        }
      }
    }
  }

  updatePixels();
}

// =========================
// TEXT GENERATION
// =========================
function generateText(txt) {
  points = [];
  state = "idle";

  let xOffset = -txt.length * 60;

  for (let i = 0; i < txt.length; i++) {
    let letter = txt[i];
    let grid = font[letter];
    if (!grid) continue;

    for (let y = 0; y < grid.length; y++) {
      for (let x = 0; x < grid[y].length; x++) {
        if (grid[y][x] === 1) {
          let px = x * spacing + xOffset;
          let py = y * spacing - 50;
          points.push(new Point(px, py));
        }
      }
    }

    xOffset += grid[0].length * spacing + letterSpacing;
  }
}

